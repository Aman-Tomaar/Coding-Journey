x = 3.14
y = -4
z = 5

print( round (x) )     # Round of decimal
print( abs (y) )       # Absolute value (distance of a number from 0)
print( pow (y,z) )     # Power = y to the power z 
print( max (x, y, z) ) # Finds the max num from given number
print( min (x, y, z) ) # Finds the min num from given number